LETTERS = sorted(set(v[0].upper() for v in RANGES.values()))

def get_ranges_by_letter(letter: str) -> list[dict]:
    """Return ranges starting with given letter."""
    return [
        {"id": k, "name": v}
        for k, v in RANGES.items()
        if v.upper().startswith(letter.upper())
    ]

def get_range_id(name: str) -> str | None:
    """Get range ID by exact name (case-insensitive)."""
    return NAME_TO_ID.get(name.lower())
